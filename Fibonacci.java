import java.util.*;

class Fibonacci
{
public static void main(String[] args)
{
int n , a=0, b=1, c;
Scanner sc = new Scanner(System.in);
System.out.println("enter number of terms :");
n=sc.nextInt();
System.out.println("Fibonacci series:");
for(int i=1;i<=n;i++)
{
System.out.println(a+"");
c=a+b;
a=b;
b=c;
}
}
}